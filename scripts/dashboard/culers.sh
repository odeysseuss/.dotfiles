#!/usr/bin/env bash

RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m'

cols=$(tput cols)

visca_barca=(
    "█╗    ██╗██╗███████╗ ██████╗ █████╗   ██████╗  █████╗ ██████╗  ██████╗ █████╗ "
    "██║   ██║██║██╔════╝██╔════╝██╔══██╗  ██╔══██╗██╔══██╗██╔══██╗██╔════╝██╔══██╗"
    "██║   ██║██║███████╗██║     ███████║  ██████╔╝███████║██████╔╝██║     ███████║"
    "╚██╗ ██╔╝██║╚════██║██║     ██╔══██║  ██╔══██╗██╔══██║██╔══██╗██║     ██╔══██║"
    " ╚████╔╝ ██║███████║╚██████╗██║  ██║  ██████╔╝██║  ██║██║  ██║╚██████╗██║  ██║"
    "  ╚═══╝  ╚═╝╚══════╝ ╚═════╝╚═╝  ╚═╝  ╚═════╝ ╚═╝  ╚═╝╚═╝  ╚═╝ ╚═════╝╚═╝  ╚═╝"
)

max_len=0
for line in "${visca_barca[@]}"; do
    (( ${#line} > max_len )) && max_len=${#line}
done

padding=$(( (cols - max_len) / 2 ))

echo -e "\n"

for line in "${visca_barca[@]}"; do
    printf "%*s" $padding ""
    visca_part="${line:0:${#line}/2}"
    barca_part="${line:${#line}/2}"
    echo -e "${BLUE}${visca_part}${NC}${RED}${barca_part}${NC}"
done

echo -e "\n"
